/* ======================================================================== */
/*  1文字描画                                                               */
/* ======================================================================== */
void gfx_putchar(int x, int y, char ch, u8 color)
{
    int row, col;
    const u8 *glyph;
    int idx;

    idx = (int)ch - 0x20;
    if (idx < 0 || idx >= 95) return;

    glyph = font8x8[idx];

    for (row = 0; row < FONT_H; row++) {
        u8 bits = glyph[row];
        for (col = 0; col < FONT_W; col++) {
            if (bits & (0x80 >> col)) {
                gfx_pixel(x + col, y + row, color);
            }
        }
    }
}

/* ======================================================================== */
/*  文字列描画                                                              */
/* ======================================================================== */
void gfx_puts(int x, int y, const char *str, u8 color)
{
    int cx = x;
    while (*str) {
        if (*str == '\n') {
            cx = x;
            y += FONT_H + 1;
        } else {
            gfx_putchar(cx, y, *str, color);
            cx += FONT_W;
        }
        str++;
    }
}

/* ======================================================================== */
/*  簡易printf (可変引数)                                                   */
/*  対応書式: %d, %u, %x, %s, %%                                           */
/* ======================================================================== */

/* 数値→文字列変換 (内部用) */
static void itoa_buf(char *buf, int val, int base, int is_signed)
{
    char tmp[12];
    int i = 0, neg = 0;
    unsigned uval;

    if (is_signed && val < 0) {
        neg = 1;
        uval = (unsigned)(-val);
    } else {
        uval = (unsigned)val;
    }

    if (uval == 0) {
        tmp[i++] = '0';
    } else {
        while (uval > 0) {
            unsigned d = uval % base;
            tmp[i++] = (d < 10) ? ('0' + d) : ('a' + d - 10);
            uval /= base;
        }
    }
    if (neg) tmp[i++] = '-';

    /* 逆順コピー */
    {
        int j;
        for (j = 0; j < i; j++) buf[j] = tmp[i - 1 - j];
        buf[i] = '\0';
    }
}

/* stdarg.h 非依存の可変引数アクセス (cdecl/watcall共通) */
void gfx_printf(int x, int y, u8 color, const char *fmt, ...)
{
    const char *p = fmt;
    /* cdecl: 引数はスタック上に右→左で積まれる */
    u32 *args = (u32 *)(&fmt) + 1;
    int arg_idx = 0;
    int cx = x;
    char buf[16];

    while (*p) {
        if (*p == '%') {
            p++;
            if (*p == 'd') {
                itoa_buf(buf, (int)args[arg_idx++], 10, 1);
                gfx_puts(cx, y, buf, color);
                cx += 0;
                { const char *s = buf; while (*s) { cx += FONT_W; s++; } }
            } else if (*p == 'u') {
                itoa_buf(buf, (int)args[arg_idx++], 10, 0);
                gfx_puts(cx, y, buf, color);
                { const char *s = buf; while (*s) { cx += FONT_W; s++; } }
            } else if (*p == 'x') {
                itoa_buf(buf, (int)args[arg_idx++], 16, 0);
                gfx_puts(cx, y, buf, color);
                { const char *s = buf; while (*s) { cx += FONT_W; s++; } }
            } else if (*p == 's') {
                const char *s = (const char *)args[arg_idx++];
                gfx_puts(cx, y, s, color);
                while (*s) { cx += FONT_W; s++; }
            } else if (*p == '%') {
                gfx_putchar(cx, y, '%', color);
                cx += FONT_W;
            }
            p++;
        } else if (*p == '\n') {
            cx = x;
            y += FONT_H + 1;
            p++;
        } else {
            gfx_putchar(cx, y, *p, color);
            cx += FONT_W;
            p++;
        }
    }
}
