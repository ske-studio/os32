/* ======================================================================== */
/*  HELP.H - libos32 ヘルプシステムAPI                                       */
/*                                                                          */
/*  /usr/man/ 配下のMarkdown形式マニュアルファイルを参照する。               */
/*  全外部プログラムにリンクされるため、argv[0]からコマンド名を自動抽出し    */
/*  対応する .1 ファイルを検索・表示する。                                   */
/* ======================================================================== */
#ifndef OS32_HELP_H
#define OS32_HELP_H

#include "os32api.h"

/* マニュアルファイルの探索ディレクトリ */
#define OS32_MAN_DIR   "/usr/man"

/* マニュアルファイルの拡張子 */
#define OS32_MAN_EXT   ".1"

/* ヘルプを stdout に色付き表示する
 *   name: コマンド名 (パス付き / .bin付きでも可、自動抽出)
 *   戻り値: 0=成功, -1=マニュアルなし
 */
int os32_help_show(const char *name);

/* マニュアルファイルの存在確認
 *   戻り値: 1=あり, 0=なし
 */
int os32_help_exists(const char *name);

#endif /* OS32_HELP_H */
