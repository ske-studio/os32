/* ======================================================================== */
/*  MEMMAP.H — OS32 システムメモリマップ定数                                 */
/*                                                                          */
/*  カーネル・ページング・プログラムローダーが参照する物理/仮想アドレスを     */
/*  一元管理する。変更時はpaging.c, kernel.c, exec.hとの整合性を確認。       */
/* ======================================================================== */

#ifndef MEMMAP_H
#define MEMMAP_H

/* ====================================================================== */
/*  カーネル配置                                                            */
/* ====================================================================== */
#define MEM_1MB               0x100000UL  /* 1MB */
#define KERNEL_LOAD_ADDR      0x9000UL    /* カーネルロードアドレス */

/* ====================================================================== */
/*  カーネルヒープ                                                          */
/* ====================================================================== */
/* BSS終端(~0x25000)より十分上、旧VRAM BB(0x70000)手前に配置 */
#define KHEAP_BASE            0x40000UL   /* カーネルヒープ開始 */
#define KHEAP_SIZE            0x30000UL   /* カーネルヒープサイズ (192KB) */

/* ====================================================================== */
/*  ページング保護範囲                                                      */
/* ====================================================================== */

/* IVT/BIOSデータ領域: Read-Only */
/* ページ0 (0x0-0xFFF) はBIOSトランポリン用にR/W維持 */
#define MEM_IVT_PROT_START    0x01000UL
#define MEM_IVT_PROT_END      0x05FFFUL

/* loader.bin + pm32.bin (使用済み): Read-Only */
#define MEM_LOADER_START      0x08000UL
#define MEM_LOADER_END        0x08FFFUL

/* カーネルスタックガードページ: Not-Present */
#define MEM_STACK_GUARD       0x8F000UL
#define MEM_STACK_GUARD_END   0x8FFFFUL

/* BIOS ROM: Read-Only */
#define MEM_BIOS_ROM_START    0xF0000UL
#define MEM_BIOS_ROM_END      0xFFFFFUL

/* サーフェス/スプライト管理プール (1MB以上、約2MBの特大区画) */
/* ====================================================================== */
/*  グラフィックス関連                                                      */
/* ====================================================================== */
#define MEM_GFX_SURF_POOL     0x100000UL

/* フォントキャッシュプール (3MB地点へ移動, 約280KB) */
#define MEM_FONT_CACHE_BASE   0x300000UL

/* Unicode-JIS変換テーブル (131KB O(1) 配列) */
#define MEM_UNICODE_TABLE_BASE 0x350000UL

/* バックバッファ(128KB) 拡張メモリ */
#define MEM_GFX_BB_BASE       0x380000UL

/* ====================================================================== */
/*  外部プログラムロード関連                                                 */
/* ====================================================================== */
#define MEM_EXEC_LOAD_ADDR    0x400000UL
#define MEM_EXEC_MAX_SIZE     (0x180000UL)       /* 1.5MB */
#define MEM_EXEC_STACK_TOP    0x57F000UL         /* スタック頂上 (0x400000 + 1.5MB - 4KB) */
#define MEM_EXEC_STACK_SIZE   0x20000UL          /* スタックサイズ 128KB */

#define MEM_EXEC_HEAP_BASE    0x580000UL         /* プログラム用ヒープ開始 (スタックの上に配置) */

/* デフォルトヒープ割り当てサイズ */
#define MEM_EXEC_DEFAULT_HEAP_SIZE 0x80000UL     /* 512KB */

/* デフォルトスタックサイズ (OS32Xヘッダに指定がない場合) */
#define MEM_EXEC_DEFAULT_STACK_SIZE 0x10000UL    /* 64KB */

/* ====================================================================== */
/*  タイマー設定                                                            */
/* ====================================================================== */
#define PIT_HZ                100   /* タイマー割り込み周波数 (Hz) */

#endif /* MEMMAP_H */
