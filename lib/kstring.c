/* ======================================================================== */
/*  KSTRING.C — カーネル用 文字列/メモリ操作ユーティリティ                  */
/*                                                                          */
/*  4バイト単位の転送で効率化したメモリ操作と、基本的な文字列操作を提供。    */
/*  libc互換シンボル (memcpy, memset, strlen) もここで定義する。             */
/* ======================================================================== */

#include "kstring.h"

/* アセンブリ版に移行したため、kmemcpy と kmemset のC言語実装は削除 */

/* ======================================================================== */
/*  kstrlen — 文字列長                                                      */
/* ======================================================================== */
u32 kstrlen(const char *s)
{
    const char *p = s;
    while (*p) p++;
    return (u32)(p - s);
}

/* ======================================================================== */
/*  kstrncpy — 最大nバイトコピー, 常にNUL終端                               */
/* ======================================================================== */
char *kstrncpy(char *dst, const char *src, u32 n)
{
    u32 i;
    if (n == 0) return dst;
    for (i = 0; i < n - 1 && src[i]; i++) {
        dst[i] = src[i];
    }
    dst[i] = '\0';
    return dst;
}

/* ======================================================================== */
/*  kstrcpy — 文字列コピー (サイズ制限なし)                                    */
/* ======================================================================== */
char *kstrcpy(char *dst, const char *src)
{
    char *d = dst;
    while ((*d++ = *src++) != '\0');
    return dst;
}

/* ======================================================================== */
/*  kstrcmp — 文字列比較                                                    */
/* ======================================================================== */
int kstrcmp(const char *a, const char *b)
{
    while (*a && *a == *b) {
        a++;
        b++;
    }
    return (unsigned char)*a - (unsigned char)*b;
}

/* ======================================================================== */
/*  kstrncmp — 最大nバイト文字列比較                                        */
/* ======================================================================== */
int kstrncmp(const char *a, const char *b, u32 n)
{
    u32 i;
    for (i = 0; i < n; i++) {
        if (a[i] != b[i]) return (unsigned char)a[i] - (unsigned char)b[i];
        if (a[i] == '\0') return 0;
    }
    return 0;
}

/* ======================================================================== */
/*  kstrcat — 文字列連結                                                      */
/* ======================================================================== */
char *kstrcat(char *dst, const char *src)
{
    char *d = dst;
    while (*d) d++;
    while ((*d++ = *src++) != '\0');
    return dst;
}

/* ======================================================================== */
/*  kstrncat — バッファ全体サイズ n を考慮した安全な文字列連結               */
/*  dst のバッファサイズが n のとき、n-1 文字目まで書き込み NUL 終端する。    */
/* ======================================================================== */
char *kstrncat(char *dst, const char *src, u32 n)
{
    u32 dlen = kstrlen(dst);
    u32 i;
    if (dlen >= n - 1) return dst;
    for (i = 0; src[i] && (dlen + i) < n - 1; i++) {
        dst[dlen + i] = src[i];
    }
    dst[dlen + i] = '\0';
    return dst;
}

/* ======================================================================== */
/*  libc互換シンボル                                                        */
/*  GCC -ffreestanding は構造体代入や初期化子で暗黙に memcpy/memset を       */
/*  呼び出すことがある。リンクエラー防止のため標準名でも提供する。           */
/* ======================================================================== */


u32 strlen(const char *s)
{
    return kstrlen(s);
}
