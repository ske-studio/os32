/* ======================================================================== */
/*  KSTRING.C — カーネル用 文字列/メモリ操作ユーティリティ                  */
/*                                                                          */
/*  4バイト単位の転送で効率化したメモリ操作と、基本的な文字列操作を提供。    */
/*  libc互換シンボル (memcpy, memset, strlen) もここで定義する。             */
/* ======================================================================== */

#include "kstring.h"

/* ======================================================================== */
/*  kmemcpy — メモリコピー (前方コピー, overlapは呼び出し側で保証)           */
/* ======================================================================== */
void *kmemcpy(void *dst, const void *src, u32 n)
{
    u8 *d = (u8 *)dst;
    const u8 *s = (const u8 *)src;
    u32 words, rem;

    /* 両方4バイトアライメントなら4バイト単位で転送 */
    if (((u32)d & 3) == 0 && ((u32)s & 3) == 0) {
        u32 *d32 = (u32 *)d;
        const u32 *s32 = (const u32 *)s;
        words = n >> 2;
        rem = n & 3;
        while (words--) {
            *d32++ = *s32++;
        }
        d = (u8 *)d32;
        s = (const u8 *)s32;
        while (rem--) {
            *d++ = *s++;
        }
    } else {
        while (n--) {
            *d++ = *s++;
        }
    }
    return dst;
}

/* ======================================================================== */
/*  kmemset — メモリフィル                                                  */
/* ======================================================================== */
void *kmemset(void *dst, int val, u32 n)
{
    u8 *d = (u8 *)dst;
    u8 byte = (u8)(val & 0xFF);
    u32 words, rem;

    if (((u32)d & 3) == 0 && n >= 4) {
        u32 fill = (u32)byte | ((u32)byte << 8) |
                   ((u32)byte << 16) | ((u32)byte << 24);
        u32 *d32 = (u32 *)d;
        words = n >> 2;
        rem = n & 3;
        while (words--) {
            *d32++ = fill;
        }
        d = (u8 *)d32;
        while (rem--) {
            *d++ = byte;
        }
    } else {
        while (n--) {
            *d++ = byte;
        }
    }
    return dst;
}

/* ======================================================================== */
/*  kstrlen — 文字列長                                                      */
/* ======================================================================== */
u32 kstrlen(const char *s)
{
    const char *p = s;
    while (*p) p++;
    return (u32)(p - s);
}

/* ======================================================================== */
/*  kstrncpy — 最大nバイトコピー, 常にNUL終端                               */
/* ======================================================================== */
char *kstrncpy(char *dst, const char *src, u32 n)
{
    u32 i;
    if (n == 0) return dst;
    for (i = 0; i < n - 1 && src[i]; i++) {
        dst[i] = src[i];
    }
    dst[i] = '\0';
    return dst;
}

/* ======================================================================== */
/*  kstrcmp — 文字列比較                                                    */
/* ======================================================================== */
int kstrcmp(const char *a, const char *b)
{
    while (*a && *a == *b) {
        a++;
        b++;
    }
    return (unsigned char)*a - (unsigned char)*b;
}

/* ======================================================================== */
/*  kstrncmp — 最大nバイト文字列比較                                        */
/* ======================================================================== */
int kstrncmp(const char *a, const char *b, u32 n)
{
    u32 i;
    for (i = 0; i < n; i++) {
        if (a[i] != b[i]) return (unsigned char)a[i] - (unsigned char)b[i];
        if (a[i] == '\0') return 0;
    }
    return 0;
}

/* ======================================================================== */
/*  libc互換シンボル                                                        */
/*  GCC -ffreestanding は構造体代入や初期化子で暗黙に memcpy/memset を       */
/*  呼び出すことがある。リンクエラー防止のため標準名でも提供する。           */
/* ======================================================================== */
void *memcpy(void *dst, const void *src, u32 n)
{
    return kmemcpy(dst, src, n);
}

void *memset(void *dst, int val, u32 n)
{
    return kmemset(dst, val, n);
}

u32 strlen(const char *s)
{
    return kstrlen(s);
}
